# Yes, Dequeue (pronounced "deck") is a data structure, and its name stands for Double-Ended Queue. It is a generalized form of a queue that allows insertion and deletion of elements from both ends (front and rear).

# Key Features of a Dequeue
# Bidirectional Operations:
# Elements can be added to or removed from both the front and rear ends.
# Dynamic Size:
# The size of a dequeue is dynamic, similar to queues and stacks.


# Operations in a Dequeue
# Insert at the front: Add an element at the front.
# Insert at the rear: Add an element at the rear.
# Delete from the front: Remove an element from the front.
# Delete from the rear: Remove an element from the rear.
# Peek front: View the element at the front without removing it.
# Peek rear: View the element at the rear without removing it.
# Check if empty: Determine whether the dequeue has any elements.
# Check size: Return the number of elements in the dequeue.
