# A priority queue is a specialized data structure in which each element is associated with a priority, and elements are dequeued (or accessed) based on their priority rather than their order of insertion.

# Key Features of a Priority Queue:
# Priority-Based Access:

# Elements with higher priority are served before those with lower priority, regardless of the insertion order.
# If two elements have the same priority, they may be dequeued based on the order of insertion (depending on the implementation).
# Operations:

# Insert (enqueue): Add an element to the queue with an associated priority.
# Remove (dequeue): Remove and return the element with the highest (or lowest, depending on the convention) priority.
# Dynamic Prioritization:

# The priorities of elements can sometimes be changed dynamically (depending on the implementation).